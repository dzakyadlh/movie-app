class Movie {
  final String id;
  final String name;
  final String imageUrl;
  final String casts;
  final String category;
  final String genre;
  final String releaseYear;

  Movie({
    required this.id,
    required this.name,
    required this.imageUrl,
    required this.casts,
    required this.category,
    required this.genre,
    required this.releaseYear,
  });
}
